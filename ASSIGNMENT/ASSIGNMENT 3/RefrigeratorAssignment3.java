    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String editName = jTextField1.getText();
        String editBrand = jTextField2.getText();
        r.get(row).refrigeratorName = editName;
        r.get(row).refrigeratorBrand = editBrand;
        dtm.setRowCount(0);
        for (int i = 0; i < r.size(); i++)
        {
            Object[] obj = {r.get(i).refrigeratorName, r.get(i).refrigeratorBrand};
            dtm.addRow(obj);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

