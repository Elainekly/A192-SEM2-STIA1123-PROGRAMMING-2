    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String refrigeratorName = jTextField1.getText();
        String refrigeratorBrand = jTextField2.getText();
        r.add(new RefrigeratorA3(refrigeratorName, refrigeratorBrand));
        dtm.setRowCount(0);
        for (int i = 0; i < r.size(); i++)
        {
            Object[] obj = {r.get(i).refrigeratorName, r.get(i).refrigeratorBrand};
            dtm.addRow(obj);
        }
        clearField();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int del = JOptionPane.YES_NO_OPTION;
        int result = JOptionPane.showConfirmDialog(this, "Delete this data", "Delete", del);
        if (result == 0)
        {
            dtm.removeRow(row);
            r.remove(row);
            dtm.setRowCount(0);
            for (int i = 0; i < r.size(); i++)
            {
                Object[] obj = {r.get(i).refrigeratorName, r.get(i).refrigeratorBrand};
                dtm.addRow(obj);
            }
            clearField();
        }
        else{
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String input = JOptionPane.showInputDialog(this, "Search Refrigerator Name:");
        for (int i = 0; i < r.size(); i++)
        {
            if (r.get(i).refrigeratorName.equalsIgnoreCase(input))
            {
                JOptionPane.showMessageDialog(jButton4, "Found!", "Search Refrigerator", 2);
                jTextField1.setText(r.get(i).refrigeratorName);
                jTextField2.setText(r.get(i).refrigeratorBrand);
                return;
            }
        }
        JOptionPane.showMessageDialog(jButton4, "Not found!", "Search refrigerator", 2);
    }//GEN-LAST:event_jButton4ActionPerformed

